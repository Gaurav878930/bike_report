9. Customer lifeline value(CLV)
-- Calculate the lifetime value of each customer and
-- Identify the top 10 customers by total revenue

SELECT  c.customer_id,c.first_name,
		count(DISTINCT o.order_id) AS total_orders,
		Round(SUM(
	   (oi.quantity*oi.list_price*(1-oi.discount)
	   )),2) AS  customer_lifetime_value
FROM customer AS c
JOIN orders AS o
ON c.customer_id=o.customer_id
JOIN order_item AS oi
ON o.order_id=oi.order_id
GROUP BY 1,2
ORDER BY customer_lifetime_value DESC 
LIMIT 10

10. Store and Category Revenue Contribution
-- For each store, calculate what percentage of total store revenue 
-- comes from each category

WITH category_revenue AS (
SELECT st.store_id,st.store_name,
		c.category_name,
		Round(SUM(
	   (oi.quantity*oi.list_price*(1-oi.discount)
	   )),2) AS  revenue 
FROM store AS st
JOIN orders AS o 
ON st.store_id=o.store_id
JOIN order_item AS oi
ON o.order_id=oi.order_id
JOIN product AS p
ON p.product_id=oi.product_id 
JOIN categories AS c
ON c.category_id=p.category_id
GROUP BY 1,2,3
)
SELECT  store_id,
		store_name,category_name,
		revenue,
	round(revenue*100/SUM(revenue)OVER(PARTITION BY  store_id)),2
	AS revenue_percentage 
FROM category_revenue 
ORDER BY  store_id,revenue_percentage  th
