6. Best-selling product in each category 
-- for every category, find the product with the highest sales 
-- quantity 

WITH product_sales AS (
SELECT  c.category_id,c.category_name,
		p.product_name,
		SUM(oi.quantity) AS Total_quantity, 
DENSE_RANK() OVER(PARTITION BY c.category_name 
		ORDER BY  SUM(oi.quantity) DESC) AS RANK
FROM order_item AS oi
JOIN product AS p
ON  oi.product_id = p.product_id 
JOIN categories AS c
ON  p.category_id=c.category_id 
GROUP BY 1,2,3
)
SELECT category_id,category_name,
		product_name,total_quantity
FROM product_sales 
WHERE RANK =1

7. Monthly revenue growth 
-- Calculate the month-over-month revenue growth percentage
-- for each store 

with monthly_sale as(
select  s.store_id,s.store_name,
		Date_trunc('month',o.order_date) as month,
		Round(SUM((oi.quantity*oi.list_price*(1-oi.discount)))
	   ) AS revenue
FROM store AS s 
JOIN orders AS o
ON s.store_id=o.store_id
join order_item AS oi
on o.order_id = oi.order_id
group by 1,2,3
)
select store_id,store_name,
	   month, revenue,
	   lag(revenue) over(partition by store_id order by month) 
	   as previous_month_revenue,
round(
	(revenue-lag(revenue)over(partition	by store_id order by month desc))
	/
	lag(revenue) over(partition by store_id order by month)*100,2)
	as growth_percentage
from monthly_sale
order by store_id desc


