1. Top-selling product 
-- Find the top 10 products based on total quantity sold across 
-- all stores  

SELECT p.product_id,
	   p.product_name,
	   sum(ot.quantity) AS total_quantity 
FROM 
  	product AS p
JOIN 
	order_item AS ot
ON p.product_id = ot.product_id
GROUP BY 1,2 
ORDER BY total_quantity DESC
LIMIT 10



2 Revenue by category 
-- calculate the total revenue generated by each product category
-- after considering discounts.

SELECT c.category_id,
	   c.category_name,
	   Round(SUM(
	   (oi.quantity*oi.list_price*(1-oi.discount)
	   )),2) AS  Revenue_generated
FROM
	product AS p
JOIN categories AS c
	ON p.category_id = c.category_id
JOIN order_item as oi 
	ON  p.product_id=oi.product_id
group by 1,2
order by Revenue_generated desc 

3 store-wise sales performances 
-- Find the total sales revenue generated by each store and 
-- rank the stores from highest to lowest revenue 

SELECT st.store_name, 
	Round(SUM(
	   (oi.quantity*oi.list_price*(1-oi.discount)
	   )),2) AS  Revenue_generated,
RANK() OVER(
		ORDER BY SUM(oi.quantity*oi.list_price*(1-oi.discount))
		desc) as rank_store
FROM store AS st
JOIN 
	orders AS o
ON st.store_id=o.store_id
JOIN
	order_item AS oi
ON o.order_id=oi.order_id 
GROUP BY store_name 

4 Customer order analysis 
-- Identify customers who have placed more than 5 orders and 
-- show their total spending 

select c.first_name,c.last_name,
	   count(oi.order_id) AS Total_orders,
	   Round(SUM((oi.quantity*oi.list_price*(1-oi.discount)))
	   ) AS Total_spending
FROM customer AS c
JOIN 
	orders AS o
ON c.customer_id = o.customer_id
JOIN
	order_item AS oi
ON o.order_id = oi.order_id 
GROUP BY  1,2
HAVING (COUNT(oi.order_id)>=5)
ORDER BY total_orders DESC	


